package _03_Casting_Wrapper;

public class Office_1_IntegertoDouble {

	public static void main(String[] args) {
		
		/* Soru 4:
		 * Integer bir degisken olusturun ve onu double degisken tipine cevirin ve konsolda yazdirin.
           Input: 25
		 */
		
		// Kucuk data type'inin buyuk data type'ina cast edilmesi
		// Auto Widening(Genisletme) Casting
		
		int i = 25;
		double d = i;
		
		System.out.println(d);
		
		
		
	}

}
